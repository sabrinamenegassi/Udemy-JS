// Não podemos criar variáveis com palavras reservadas
// Variáveis precisam ter nomes significativos
// Não pode começar o nome de uma variável com um número
// Não podem conter espaços ou traços
// Utilizamos camelCase
// Case-sensitive
// Não podemos redeclarar variáveis com let
// NÃO UTILIZE VAR, UTILIZE LET.
let nomeCliente = 'Sabrina';
console.log(nomeCliente);
nomeCliente = 'Fernanda';
console.log(nomeCliente);

let nome = 'José';
let nomeFem = 'Joana';
let nomeFilho = 'Chico';

console.log(nome, 'nasceu em 1984.');
console.log('Em 2000', nome, 'conheceu', nomeFem);
console.log(nome, 'casou-se com', nomeFem, 'em 2012.');
console.log(nomeFem, 'teve 1 filho com', nome, 'em 2015.');
console.log('O filho de', nome, 'se chama', nomeFilho);